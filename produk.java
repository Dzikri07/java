    public void refreshTable() {
    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_uts2", "root", "");

        String query = "SELECT tprodukbuku.Kode_Buku, Trak.Kode_Rak, tprodukbuku.Nama_Produk, " +
                       "tprodukbuku.Penerbit, tprodukbuku.Pengarang, tprodukbuku.Harga " +
                       "FROM tprodukbuku " +
                       "JOIN Trak ON tprodukbuku.Kode_Rak = Trak.Kode_Rak"; // JOIN diperbaiki

        PreparedStatement ps = con.prepareStatement(query);
        ResultSet rs = ps.executeQuery();

        DefaultTableModel model = (DefaultTableModel) Tprod.getModel();
        model.setRowCount(0); // Hapus data lama di tabel

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("Kode_Buku"),  // Ambil dari tprodukbuku
                rs.getString("Kode_Rak"),   // Diambil dari tabel Trak
                rs.getString("Nama_Produk"),
                rs.getString("Penerbit"),
                rs.getString("Pengarang"),
                rs.getInt("Harga") // Harga pakai getInt karena INT
            });
        }

        // Tutup koneksi
        rs.close();
        ps.close();
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
}


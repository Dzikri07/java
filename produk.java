    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // Ambil nilai dari inputan
  String kodeBuku = fkodeb.getText().trim();
  String namaBuku = fnama.getText().trim();
  String penerbit = fpen.getText().trim();
  String pengarang = fpeng.getText().trim();
  String hargaStr = fharga.getText().trim(); // Harga masih dalam bentuk String
  String kodeRak = flkode.getSelectedItem().toString().trim();

  // Periksa apakah ada field yang kosong
  if (kodeBuku.isEmpty() || kodeRak.isEmpty() || namaBuku.isEmpty() || 
      penerbit.isEmpty() || pengarang.isEmpty() || hargaStr.isEmpty()) {

      JOptionPane.showMessageDialog(null, "Data harus terisi semua!", "Peringatan", JOptionPane.WARNING_MESSAGE);

  } else if (kodeBuku.length() < 8) { // Periksa apakah Kode Buku kurang dari 8 digit
      JOptionPane.showMessageDialog(null, "Kode Buku harus 8 digit!", "Peringatan", JOptionPane.WARNING_MESSAGE);

  } else {
      try {
          // Cek apakah harga adalah angka
          int harga = Integer.parseInt(hargaStr);

          Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_uts2", "root", "");

          String cekSql = "SELECT COUNT(*) FROM tprodukbuku WHERE Kode_Buku = ?";
          PreparedStatement cekPst = conn.prepareStatement(cekSql);
          cekPst.setString(1, kodeBuku);
          ResultSet rs = cekPst.executeQuery();
          rs.next();
          int count = rs.getInt(1);
          rs.close();
          cekPst.close();

          if (count > 0) {
              int konfirmasi = JOptionPane.showConfirmDialog(this, "Data sudah ada. Update?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
              if (konfirmasi == JOptionPane.YES_OPTION) {
                  String updateSql = "UPDATE tprodukbuku SET Nama_Produk=?, Penerbit=?, Pengarang=?, Harga=?, Kode_Rak=? WHERE Kode_Buku=?";
                  PreparedStatement updatePst = conn.prepareStatement(updateSql);
                  updatePst.setString(1, namaBuku);
                  updatePst.setString(2, penerbit);
                  updatePst.setString(3, pengarang);
                  updatePst.setInt(4, harga); // Simpan harga sebagai Integer
                  updatePst.setString(5, kodeRak);
                  updatePst.setString(6, kodeBuku);
                  updatePst.executeUpdate();
                  updatePst.close();
                  JOptionPane.showMessageDialog(this, "Data berhasil diupdate.");
              }
          } else {
              String insertSql = "INSERT INTO tprodukbuku (Kode_Buku, Nama_Produk, Penerbit, Pengarang, Harga, Kode_Rak) VALUES (?, ?, ?, ?, ?, ?)";
              PreparedStatement insertPst = conn.prepareStatement(insertSql);
              insertPst.setString(1, kodeBuku);
              insertPst.setString(2, namaBuku);
              insertPst.setString(3, penerbit);
              insertPst.setString(4, pengarang);
              insertPst.setInt(5, harga); // Simpan harga sebagai Integer
              insertPst.setString(6, kodeRak);
              insertPst.executeUpdate();
              insertPst.close();
              JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
          }

          conn.close();
          refreshTable(); //

      } catch (NumberFormatException e) {
          JOptionPane.showMessageDialog(null, "Harga harus berupa angka!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
      } catch (SQLException e) {
          JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
      }
  }

    }//GEN-LAST:event_saveActionPerformed


    private void loadKodeRak() {
    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_uts2", "root", "");
        String sql = "SELECT Kode_Rak FROM trak";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        
        flkode.removeAllItems(); // Bersihkan item sebelum diisi
        while (rs.next()) {
            flkode.addItem(rs.getString("Kode_Rak"));
        }
        
        rs.close();
        pst.close();
        conn.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error loading Kode Rak: " + e.getMessage());
    }
}
    
    

    private void tampilData() {
    DefaultTableModel model = new DefaultTableModel();  
    model.addColumn("Kode Buku");
    model.addColumn("Kode Rak");
    model.addColumn("Nama Produk");
    model.addColumn("Penerbit");
    model.addColumn("Pengarang");
    model.addColumn("Harga");

    try {
    String sql = "SELECT tprodukbuku.Kode_Buku, trak.Kode_Rak, tprodukbuku.Nama_Produk, " +
                 "tprodukbuku.Penerbit, tprodukbuku.Pengarang, tprodukbuku.Harga " +
                 "FROM tprodukbuku " +
                 "INNER JOIN trak ON tprodukbuku.Kode_Rak = trak.Kode_Rak";
    
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_uts2", "root", "");
    Statement stm = conn.createStatement();
    ResultSet res = stm.executeQuery(sql);

    while (res.next()) {
        model.addRow(new Object[]{
            res.getString("Kode_Buku"),
            res.getString("Kode_Rak"),  
            res.getString("Nama_Produk"),
            res.getString("Penerbit"),
            res.getString("Pengarang"),
            res.getString("Harga")
        });
    }
    Tprod.setModel(model);
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Gagal Menampilkan Data: " + e.getMessage());
}

}


